#include "minishell.h"

void	cd(char **cmd, t_env_list *env)
{
	(void)env;
	if (cmd[2])
		exit(1);
	if (chdir(cmd[1]) == -1)
	{
		perror("cd");
		exit(1);
	}
	exit(0);
}
